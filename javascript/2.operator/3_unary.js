//  단항 연산자 : ++, --, !, !!
let a = 10;
console.log(++a); // 1을 더한 11
console.log(a++); //a변수에 먼저11을 할당하고 1증가
console.log(a); // 12

let b = 10;
console.log(--b);// 1을 뺀 9
console.log(b--); // 9를 할당하고 1 감소
console.log(++b); // 8을 할당하고 1 증가 
console.log(b++); // 9를 넣고 1증가
console.log(b); // 10

let flag = true;
console.log(flag);
console.log(!flag);
console.log(!!flag);

